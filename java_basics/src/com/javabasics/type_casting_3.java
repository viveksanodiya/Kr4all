package com.javabasics;

public class type_casting_3 {
	public static void main(String[]args) {
		
		double a=1.123654;
		int b=(int) a;
		System.out.println(b);
		
		double c=7.36988;
		short d= (short)c;
		System.out.println(d);
		
		double e=6.2525;
		float f= (float)e;
		System.out.println(f);
		
		double g=2.123;
		char h= (char) g;
		System.out.println(h);
		
		double i= 8.654523;
		byte j=(byte)i;
		System.out.println(j);
		
		
		
		
				
		
		
		
		
	}

}
