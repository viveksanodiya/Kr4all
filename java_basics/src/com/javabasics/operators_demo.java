package com.javabasics;

public class operators_demo {
	public static void main(String[] args) {
		int a =10;
		int b =5;
		System.out.println(a+b);
		int c=a+b;
		System.out.println("c="+c);
		
		int e=11;
		int f=5;
		int g=e%f;
		System.out.println(g);// modulas
		
		int x=5;
		int y=5;
		
		System.out.println(x++);// post increment
		System.out.println(x);
		System.out.println(++y);// pre increment
		System.out.println(y);
		// hw need to try decremet 
		
		int q=5;
		int r=9;
		System.out.println(q==r);
		System.out.println(q!=r);
	
		//Java Logical Operators
		int h=9; 
	System.out.println(h < 6 &&  h < 10); //and
	System.out.println(h < 7 || h < 10); // or
	System.out.println(!(h < 8 && h < 7)); // not
	
	
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
